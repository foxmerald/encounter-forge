defmodule EncounterforgeWeb.ForgeLive do
  use EncounterforgeWeb, :live_view

  alias Encounterforge.Encounter

  @max_vibe_length 200
  @cooldown_ms 3_000

  def mount(_params, _session, socket) do
    {:ok, assign(socket, encounter: Encounter.generate_mock(), loading: false, vibe: "", last_submit_at: nil)}
  end

  def render(assigns) do
    ~H"""
    <div class="min-h-screen bg-stone-100 py-12 px-4 sm:px-6 lg:px-8 font-serif">
      <div class="max-w-3xl mx-auto">

        <header class="text-center mb-12">
          <h1 class="text-5xl font-extrabold text-stone-900 tracking-tight">
            Encounter<span class="text-red-800">Forge</span>
          </h1>
          <p class="mt-3 text-lg text-stone-600 italic">"The Forge weaves the tale - you provide the spark."</p>
        </header>

        <section class="bg-white p-8 rounded-lg shadow-md border border-stone-200">
          <form phx-submit="save_vibe" class="space-y-4">
            <div>
